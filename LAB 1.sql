														--PART-C--

-- 1. Details of customers who deposited > 5000 (without *)
SELECT ACTNO, CNAME, BNAME, AMOUNT, ADATE
FROM DEPOSIT
WHERE AMOUNT > 5000;

-- 2. Unique customer names with city
