-- 3. Borrow where amount > 3000 and loan no NOT multiple of 3
SELECT * FROM BORROW
WHERE AMOUNT > 3000 
AND LOANNO % 3 <> 0;

-- 4. Complex condition
SELECT * FROM DEPOSIT
WHERE AMOUNT > 2000
AND ACTNO BETWEEN 100 AND 110
AND (ADATE > '1995-03-01' OR ADATE < '1996-03-27');

-- 5. Odd loan numbers
SELECT * FROM BORROW
WHERE LOANNO % 2 = 1;

-- Even loan numbers
SELECT * FROM BORROW
WHERE LOANNO % 2 = 0;


